import numpy as np

# ============================================================
# روش ۱: ضرب ماتریس با حلقه‌های تو در تو (بدون NumPy)
# ============================================================

def multiply_matrices_manual(A, B):
    """
    ضرب دو ماتریس را با استفاده از حلقه‌های تو در تو محاسبه می‌کند.
    
    پارامترها:
        A (list of list): ماتریس اول
        B (list of list): ماتریس دوم
        
    بازگشت:
        list of list: حاصل ضرب دو ماتریس
    """
    # تعداد سطرها و ستون‌ها
    rows_A = len(A)
    cols_A = len(A[0])
    rows_B = len(B)
    cols_B = len(B[0])
    
    # بررسی امکان ضرب
    if cols_A != rows_B:
        raise ValueError("تعداد ستون‌های ماتریس A باید برابر با تعداد سطرهای ماتریس B باشد")
    
    # ایجاد ماتریس نتیجه با ابعاد rows_A × cols_B
    result = [[0 for _ in range(cols_B)] for _ in range(rows_A)]
    
    # حلقه‌های تو در تو برای ضرب
    for i in range(rows_A):
        for j in range(cols_B):
            for k in range(cols_A):
                result[i][j] += A[i][k] * B[k][j]
    
    return result


# ============================================================
# روش ۲: ضرب ماتریس با NumPy
# ============================================================

def multiply_matrices_numpy(A, B):
    """
    ضرب دو ماتریس را با استفاده از NumPy محاسبه می‌کند.
    
    پارامترها:
        A (numpy.ndarray): ماتریس اول
        B (numpy.ndarray): ماتریس دوم
        
    بازگشت:
        numpy.ndarray: حاصل ضرب دو ماتریس
    """
    return np.dot(A, B)  # یا A @ B


# ============================================================
# تابع نمایش ماتریس (برای چاپ زیبا)
# ============================================================

def print_matrix(matrix, title="ماتریس"):
    """
    یک ماتریس را به صورت زیبا چاپ می‌کند.
    
    پارامترها:
        matrix: ماتریس (لیست لیست یا numpy.ndarray)
        title (str): عنوان ماتریس
    """
    print(f"\n{title}:")
    print("-" * 30)
    for row in matrix:
        print("  ".join(f"{x:6}" for x in row))
    print("-" * 30)


# ============================================================
# اجرای اصلی برنامه
# ============================================================

if __name__ == "__main__":
    
    # =========================================================
    # تعریف دو ماتریس ۳×۳
    # =========================================================
    
    A = [
        [1, 2, 3],
        [4, 5, 6],
        [7, 8, 9]
    ]
    
    B = [
        [9, 8, 7],
        [6, 5, 4],
        [3, 2, 1]
    ]
    
    # تبدیل به numpy array برای روش دوم
    A_np = np.array(A)
    B_np = np.array(B)
    
    # =========================================================
    # نمایش ماتریس‌ها
    # =========================================================
    
    print_matrix(A, "ماتریس A (۳×۳)")
    print_matrix(B, "ماتریس B (۳×۳)")
    
    # =========================================================
    # روش ۱: ضرب با حلقه‌های تو در تو
    # =========================================================
    
    result_manual = multiply_matrices_manual(A, B)
    print_matrix(result_manual, "نتیجه ضرب (روش حلقه تو در تو)")
    
    # =========================================================
    # روش ۲: ضرب با NumPy
    # =========================================================
    
    result_numpy = multiply_matrices_numpy(A_np, B_np)
    print_matrix(result_numpy, "نتیجه ضرب (روش NumPy)")
    
    # =========================================================
    # مقایسه دو روش (بررسی برابری نتایج)
    # =========================================================
    
    are_equal = np.allclose(result_manual, result_numpy)
    
    print("\n" + "=" * 40)
    print("مقایسه دو روش:")
    print(f"آیا نتایج یکسان هستند؟ {'بله ✅' if are_equal else 'خیر ❌'}")
    print("=" * 40)
    
    # =========================================================
    # محاسبه دترمینان (اختیاری – برای تمرین بیشتر)
    # =========================================================
    
    det = np.linalg.det(A_np)
    print(f"\nدترمینان ماتریس A: {det:.2f}")